    async def get_coin_details_batch(self, coin_ids: list):
        """Ïó¨Îü¨ ÏΩîÏù∏Ïùò ÏÉÅÏÑ∏ Ï†ïÎ≥¥Î•º Î∞∞ÏπòÎ°ú Ï°∞Ìöå"""
        logger.info(f"üéØ {len(coin_ids)}Í∞ú ÏΩîÏù∏ ÏÉÅÏÑ∏ Ï†ïÎ≥¥ Ï°∞Ìöå...")
        
        coin_details = []
        
        for coin_id in coin_ids:
            try:
                url = f"https://api.coingecko.com/api/v3/coins/{coin_id}"
                async with self.session.get(url) as response:
                    if response.status == 200:
                        coin_data = await response.json()
                        details = {
                            'id': coin_data['id'],
                            'symbol': coin_data['symbol'].upper(),
                            'name': coin_data['name'],
                            'image_url': coin_data.get('image', {}).get('large', ''),
                            'market_cap_rank': coin_data.get('market_cap_rank'),
                            'description': coin_data.get('description', {}).get('en', '')[:500] if coin_data.get('description', {}).get('en') else ''
                        }
                        
                        if details['image_url']:
                            coin_details.append(details)
                            logger.info(f"‚úÖ {details['symbol']}: {details['image_url']}")
                        else:
                            logger.warning(f"‚ö†Ô∏è {details['symbol']}: Ïù¥ÎØ∏ÏßÄ URL ÏóÜÏùå")
                            
                    elif response.status == 429:
                        logger.warning(f"‚è∏Ô∏è Rate limit reached for {coin_id}")
                        await asyncio.sleep(5)
                    else:
                        logger.warning(f"‚ö†Ô∏è {coin_id} Ï°∞Ìöå Ïã§Ìå®: {response.status}")
                
                # Rate limiting Î∞©ÏßÄ
                await asyncio.sleep(1.2)  # 1.2Ï¥à ÎåÄÍ∏∞
                
            except Exception as e:
                logger.warning(f"‚ö†Ô∏è {coin_id} Ï°∞Ìöå Ïò§Î•ò: {e}")
                continue
        
        return coin_details
    
    def save_coin_images(self, coin_details: list):
        """ÏΩîÏù∏ Ïù¥ÎØ∏ÏßÄÎ•º Îç∞Ïù¥ÌÑ∞Î≤†Ïù¥Ïä§Ïóê Ï†ÄÏû•"""
        logger.info(f"üíæ {len(coin_details)}Í∞ú ÏΩîÏù∏ Ïù¥ÎØ∏ÏßÄ Ï†ÄÏû•...")
        
        saved_count = 0
        updated_count = 0
        failed_count = 0
        
        with db_manager.get_session_context() as session:
            for coin in coin_details:
                try:
                    # Í∏∞Ï°¥ Î†àÏΩîÎìú ÌôïÏù∏
                    existing_coin = session.query(CoinMaster).filter_by(
                        symbol=coin['symbol'],
                        is_active=True
                    ).first()
                    
                    if existing_coin:
                        # Í∏∞Ï°¥ Î†àÏΩîÎìú ÏóÖÎç∞Ïù¥Ìä∏
                        if not existing_coin.image_url or existing_coin.image_url != coin['image_url']:
                            existing_coin.image_url = coin['image_url']
                            existing_coin.updated_at = datetime.now()
                            updated_count += 1
                            logger.info(f"üîÑ {coin['symbol']}: Ïù¥ÎØ∏ÏßÄ URL ÏóÖÎç∞Ïù¥Ìä∏")
                    else:
                        # ÏÉà Î†àÏΩîÎìú ÏÉùÏÑ±
                        new_coin = CoinMaster(
                            coingecko_id=coin['id'],
                            symbol=coin['symbol'],
                            name_en=coin['name'],
                            image_url=coin['image_url'],
                            market_cap_rank=coin['market_cap_rank'],
                            description=coin['description'],
                            is_active=True,
                            created_at=datetime.now(),
                            updated_at=datetime.now()
                        )
                        session.add(new_coin)
                        saved_count += 1
                        logger.info(f"üíæ {coin['symbol']}: ÏÉà Î†àÏΩîÎìú ÏÉùÏÑ±")
                    
                except Exception as e:
                    failed_count += 1
                    logger.error(f"‚ùå {coin['symbol']} Ï†ÄÏû• Ïã§Ìå®: {e}")
                    continue
            
            session.commit()
        
        logger.info(f"‚úÖ Ï†ÄÏû• ÏôÑÎ£å: {saved_count}Í∞ú Ïã†Í∑ú, {updated_count}Í∞ú ÏóÖÎç∞Ïù¥Ìä∏, {failed_count}Í∞ú Ïã§Ìå®")
        return saved_count + updated_count

